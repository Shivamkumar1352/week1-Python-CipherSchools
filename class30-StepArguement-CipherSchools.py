#slicing /selecting subsequences
lang = "python"
#syntax - [start arguement: stop arguement:step]
print("Shivam"[2:5:1])   #output= iva
print("Shivam"[2:5])     #output= iva
print("Shivam"[2:5:2])   #output= ia  
print("Shivam"[0::2])    #output= Sia
print("Shivam"[8::-1])   #output = mavihS
print("Shivam"[::-1])    #output = mavihS (TRICK)